#include<stdio.h>
int main()
{
    
    int i=7;
    
    i++;
    printf("i = %d\n",i);
    
    i--;
    printf("i = %d\n",i);
    
    printf("i = %d\n",--i);
    
    printf("i = %d\n",++i);
    
    printf("i = %d\n",i++);
    
    getch();
	return(0);
} 

